    private void jButton2AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2AceptarActionPerformed
        // TODO add your handling code here:
        boolean ok = true;
        String msj = "";
        if (this.jTextField1Nombre.getText().isEmpty()) {
            ok = false;
            msj += "\nEl Nombre es obligatorio.";
        }
        if (this.jTextField2Ciudad.getText().isEmpty()) {
            ok = false;
            msj += "\nLa Ciudad es obligatoria.";
        }
        if (this.jTable3.getSelectedRow() < 0) {
            ok = false;
            msj += "\nDebe solucionar la Audicion";
        }
        if (!ok) {
            JOptionPane.showMessageDialog(this, "Debe solucionar los siguientes problemas:\n" + msj, "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Candidato nuevoCandidato = new Candidato();
        nuevoCandidato.setIdcandidato(CandidatoDAO.getLastId() + 1);
        nuevoCandidato.setNombre(this.jTextField1Nombre.getText());
        nuevoCandidato.setCiudad(this.jTextField2Ciudad.getText());
        nuevoCandidato.setFechainscripcion(new Date());
        nuevoCandidato.setFinalista(false);
        int idcat = ((Categoria) this.jComboBox1.getSelectedItem()).getIdcategoria();
        nuevoCandidato.setIdcategoria(idcat);
        String idAudicion = jTable3.getModel().getValueAt(jTable3.getSelectedRow(), 1).toString();
        nuevoCandidato.setIdaudicion(Integer.parseInt(idAudicion));
        nuevoCandidato = CandidatoDAO.insertarCandidato(nuevoCandidato);
        List<Persona> list;
        list = new ArrayList<Persona>();
        if (jRadioButton1.isSelected()) {
            list = GetPersonas(jTable2);
            Individual individual = new Individual();
            individual.setIdcandidato(nuevoCandidato.getIdcandidato());
            String s = jTable2.getModel().getValueAt(0, 4).toString();
            individual.setEdad(Integer.parseInt(s));
            IndividualDAO.insertarIndividual(individual);
        } else {
            list = GetPersonas(jTable4);
            Grupo grupo = new Grupo();
            grupo.setIdcandidato(nuevoCandidato.getIdcandidato());
            int idPortavoz = Integer.parseInt(jTextIdPortavoz.getText());
            Persona persona = null;
            for (Persona p : list) {
                if (p.getIdpersona() == idPortavoz) {
                    persona = p;
                    break;
                }
            }
            if (persona == null) {
                persona = list.get(0);
            }
            grupo.setIdportavoz(persona);
            GrupoDAO.insertarGrupo(grupo);
        }
        
 
        // PErsonaDaO.InsertarPErsonas(List)

    }//GEN-LAST:event_jButton2AceptarActionPerformed

