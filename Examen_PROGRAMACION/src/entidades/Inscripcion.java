    private List<Persona> GetPersonas(JTable table) {
        List<Persona> list = new ArrayList<Persona>();
        for (int i = 0; i < table.getRowCount(); i++) {
            Persona p = new Persona();
            p.setNombre(table.getValueAt(i, 0).toString());
            p.setEmail(table.getValueAt(i, 1).toString());
            p.setDni(table.getValueAt(i, 2).toString());
            p.setTelefono(table.getValueAt(i, 3).toString());
            list.add(p);
        }
        return list;
    }

